from odoo import models, fields


class StudentPortal(models.Model):
    _inherit = 'res.partner'

    is_parent = fields.Boolean("is parent")
    is_student = fields.Boolean("is student")
    student_id = fields.One2many('op.student', 'partner_id', string='Student Name')

    def _get_name(self):
        partner = self
        name = partner.filtered(lambda part: len(part.student_id) == 1) and partner.student_id.complete_name or partner.name or ''

        if partner.company_name or partner.parent_id:
            if not name and partner.type in ['invoice', 'delivery', 'other']:
                name = dict(self.fields_get(['type'])['type']['selection'])[partner.type]
            if not partner.is_company:
                name = self._get_contact_name(partner, name)
        if self._context.get('show_address_only'):
            name = partner._display_address(without_company=True)
        if self._context.get('show_address'):
            name = name + "\n" + partner._display_address(without_company=True)
        name = name.replace('\n\n', '\n')
        name = name.replace('\n\n', '\n')
        if self._context.get('address_inline'):
            name = name.replace('\n', ', ')
        if self._context.get('show_email') and partner.email:
            name = "%s <%s>" % (name, partner.email)
        if self._context.get('html_format'):
            name = name.replace('\n', '<br/>')
        if self._context.get('show_vat') and partner.vat:
            name = "%s â€’ %s" % (name, partner.vat)
        return name
